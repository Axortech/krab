#[cfg(test)]
mod tests {
    use crate::{
        discovered_static_routes, enumerate_dynamic_route_params, register_routes,
        ssg_manifest_json, Router,
    };
    use axum::body::Body;
    use axum::http::{Request, StatusCode};
    use krab_core::Render;
    use krab_macros::view;
    use tower::ServiceExt;

    #[test]
    fn ssr_snapshot_home_fragment() {
        let rendered = view! {
            <div id="snapshot-root">
                <h1>"Krab Framework"</h1>
                <p>"SSR snapshot baseline"</p>
            </div>
        }
        .render();

        let expected =
            "<div id=\"snapshot-root\"><h1>Krab Framework</h1><p>SSR snapshot baseline</p></div>";
        assert_eq!(rendered, expected);
    }

    #[test]
    fn ssg_discovery_contract_is_generated() {
        let routes = discovered_static_routes();
        assert!(routes.contains(&"/"));
        assert!(routes.contains(&"/about"));

        let params = enumerate_dynamic_route_params();
        let blog = params
            .get("/blog/{slug}")
            .expect("blog dynamic params should exist");
        assert!(!blog.is_empty());

        let manifest = ssg_manifest_json();
        assert!(manifest.contains("discovered_routes"));
        assert!(manifest.contains("asset_manifest"));
    }

    #[tokio::test]
    async fn route_level_middlewares_execute_in_declared_order() {
        let app = register_routes(Router::new(), ());
        let request = Request::builder()
            .method("GET")
            .uri("/api/middleware_probe")
            .body(Body::empty())
            .unwrap();

        let response = app.oneshot(request).await.unwrap();
        assert_eq!(response.status(), StatusCode::OK);
        assert_eq!(
            response
                .headers()
                .get("x-krab-route-mw-order")
                .and_then(|v| v.to_str().ok()),
            Some("middleware_probe_first,middleware_probe_second")
        );
    }

    #[tokio::test]
    async fn route_level_middlewares_can_short_circuit() {
        let app = register_routes(Router::new(), ());
        let request = Request::builder()
            .method("GET")
            .uri("/api/middleware_probe")
            .header("x-krab-route-mw-short-circuit", "1")
            .body(Body::empty())
            .unwrap();

        let response = app.oneshot(request).await.unwrap();
        assert_eq!(response.status(), StatusCode::FORBIDDEN);
        assert_eq!(
            response
                .headers()
                .get("x-krab-route-mw-order")
                .and_then(|v| v.to_str().ok()),
            Some("middleware_probe_first,middleware_probe_second")
        );
    }
}
